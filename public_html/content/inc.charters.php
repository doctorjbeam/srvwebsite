	<h1>Charter options</h1>
	<p>Steamrail can organise heritage trains tailored to suit any event, and we cater for groups ranging in size from 50 to 1,000 people.</p>
	<p>Whether running suburban steam shuttles for a local traders' association, moving hundreds of school children to a regional centre, or a sleeping berth train covering thousands of kilometres over days, no job is too big or too small.</p>
	<p>Send an email to <a href="mailto:charter@steamrail.com.au">charter@steamrail.com.au</a> for your next railway adventure.</p>
	
