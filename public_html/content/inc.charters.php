	<h2>Destinations</h2>
	
	<span class="well-img" style="background-image: url(http://www.steamrail.com.au/images/r761ballarattrailing.JPG);"></span>
	
	<p>Steamrail can operate trains throughout Victoria and into southern New South Wales. </p>

	<p>The chartering of trains is possible, however destination availability will be affected by circumstances beyond our control.</p>
	
	<p>Contact us for further information and details on where and when charter train may operate.</p>
	
	<p>Steamrail Victoria is a Not-For-Profit voluntary organisation that restores and operates various examples of Victorian Railway rolling stock.</p>
	
	<p>The operation of these vehicles is made possible with the co-operation of the Victrack, the Department of Transport and V/Line Corporation Pty Ltd.</p>
