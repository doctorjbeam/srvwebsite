	<h1>Become a member of Steamrail</h1>
	<p>Steamrail Victoria is a not-for-profit organisation dedicated to the restoration and operation of historic railway vehicles. This includes steam, diesel and electric locomotives and carriages. These trains operate throughout the state. We invite you to become a member of Steamrail and enjoy the benefits:</p>
	<ul>
		<li>All forthcoming brochures</li>
		<li>Discounts for all Steamrail Trains</li>
		<li>Our monthly Steamrail Newsletter</li>
		<li>You can assist in the restoration and operation of our trains</li>
	</ul>
	<p>Single or Family membership is available. Family membership allows you to enjoy your hobby with your partner but receive only one copy of the newsletter. Please note that both members must have the same address. To receive tour brochures without the other benefits, you may subscribe to our mailing list only. Please note that persons under 16 can only be admitted as associate members, and applications must be accompanied by date of birth, with a parent or guardian signature.</p>
	<h2>How to apply</h2>
	<p>Click <a href="http://www.steamrail.com.au/documents/SRV_Membership_Brochure_Web.pdf">here</a> to download the membership application form.</p>
	<p>Complete the form and mail it to:</p>

	<p><strong>Membership Officer</strong><br>
	Steamrail Victoria<br>
	PO Box 125<br>
	Newport VIC 3015</p>
