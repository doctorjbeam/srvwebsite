	<h2>Volunteers needed!</h2>
	<span class="well-img" style="background-image: url(http://www.steamrail.com.au/images/jamesr761.jpg);"></span>
	<p>Steamrail relies on a small core of highly dedicated specialist staff supported by a large team of volunteers. They all work to restore, maintain and operate the extensive fleet of locomotives and rolling stock as well as the associated depot and support facilities.</p>
	<p>It doesn't matter what you do or have done in your day job, we need and can use your assistance. Everything from skilled engineering to clerical, gardening and cleaning tasks need to be done and in most cases, no previous railway experience is required. (In any case, you can soon gain this experience and training along with the satisfaction of being part of it all.)</p>
	<p>Click <a href="http://www.steamrail.com.au/?page=volunteer">here</a> for more information...</p>
