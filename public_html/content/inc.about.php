	<h1>About us</h1>
	<p>Steamrail Victoria is a non-profit organisation dedicated to the restoration and operation of vintage steam, diesel and electric locomotives and carriages.</p>

	<p>The Vintage Train&copy; operates monthly to destinations throughout the state. It travels all over the Victorian Railways broad gauge network offering a variety of tours for all tastes, including weekend excursions to interesting and popular destinations.</p>
	
	<p>The Steamrail carriage fleet comprises some thirty cars including sitting, sleeping and specialist vehicles. These vehicles date back to the early 20th century and feature comfortable seating, varnished wood panelling and opening windows. Trains have a kiosk car and most include a licensed bar.</p>
	
	<p>The Newport Railway Workshops were built in the 1880s and were actually the birthplace of many of our locomotives and carriages. These facilities enable us to keep our historic fleet in tiptop shape, and to carry out current restoration projects such as the precious A2 steam locomotive and vintage diesel B72.</p>
	
	<p>We welcome new members as well as new volunteer workers. We are always in need of assistance in a huge variety of tasks that keep Steamrail operating. People interested in visiting the workshops to see preservation in action are always welcome.</p>
	
